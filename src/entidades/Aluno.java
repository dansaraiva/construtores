package entidades;

public class Aluno {
	private String mat;
	private int idade;
	private boolean farda;
	public String getMat() {
		return mat;
	}
	public void setMat(String mat) {
		this.mat = mat;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public boolean isFarda() {
		return farda;
	}
	public void setFarda(boolean farda) {
		this.farda = farda;
	}
	
	
	
}
